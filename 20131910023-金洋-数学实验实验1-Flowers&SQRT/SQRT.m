function answer=SQRT(a)
%迭代公式 X(n+1)=(X(n)+a/X(n))/2;n=0,1,2
%使用for 循环，迭代10次；一旦循环中相邻两个迭代值的绝对值小于10^-5时结束迭代
%此处设x(0)=2,matlab中向量的下标从1开始，故先计算出x(1);
x(1)=(2+a/2)/2;
for i=1:9
    x(i+1)=1/2*(x(i)+a/x(i));
    %若没有此break判断语句，则在迭代10次之后结束迭代运算，得到结果；
    if (abs(x(i+1)-x(i))<10^-5) break;
    end
end;
%由于中途可能跳出，故返回值不一定是x(10),而应该是x(i+1);
answer=x(i+1);