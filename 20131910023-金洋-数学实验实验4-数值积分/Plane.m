x0=[0	3	5	7	9	11	12	13	14	15];
y1=[0	1.8	2.2	2.7	3.0	3.1	2.9	2.5	2.0	1.6];
y2=[0	1.2	1.7	2.0	2.1	2.0	1.8	1.2	1.0	1.6];
x=0:0.1:15;
yy1=spline(x0,y1,x);%上轮廓线三次样条插值法的结果
yy2=spline(x0,y2,x);%下轮廓线三次样条插值法的结果
[x',yy1',yy2']%输出在xoy坐标系下的结果
subplot(2,1,1),plot(x0,y1,x0,y2),title('原轮廓图')%画出原轮廓图
subplot(2,1,2),plot(x,yy1,x,yy2),title('加密后轮廓图')%画出三次样条插值法结果的轮廓图
S=trapz(x,yy1)-trapz(x,yy2)%上轮廓线与x轴围成的面积减去下轮廓线与x轴所围成的面积，即为剖面面积；
